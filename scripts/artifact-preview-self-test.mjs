#!/usr/bin/env node

import fs from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';

const ROOT_DIR = process.cwd();
const DEFAULT_FIXTURE_DIR = 'artifact-preview-self-test';

function parseArgs(argv) {
  const result = {
    outDir: null,
    fixtureDir: DEFAULT_FIXTURE_DIR,
    printOnly: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === '--out-dir') {
      result.outDir = argv[index + 1] ?? null;
      index += 1;
      continue;
    }
    if (token === '--fixture-dir') {
      result.fixtureDir = argv[index + 1] ?? DEFAULT_FIXTURE_DIR;
      index += 1;
      continue;
    }
    if (token === '--print-only') {
      result.printOnly = true;
    }
  }

  return result;
}

async function readJson(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return JSON.parse(raw);
}

async function resolveDefaultWorkspaceDir() {
  const tauriConfigPath = path.join(ROOT_DIR, 'apps/desktop/src-tauri/tauri.generated.conf.json');
  const brandConfigPath = path.join(ROOT_DIR, 'apps/desktop/src-tauri/brand.generated.json');
  const tauriConfig = await readJson(tauriConfigPath);
  const brandConfig = await readJson(brandConfigPath);
  const identifier = tauriConfig.identifier || brandConfig.bundleIdentifier;

  if (!identifier || typeof identifier !== 'string') {
    throw new Error('Unable to resolve desktop bundle identifier from generated config');
  }

  const homeDir = os.homedir();
  switch (process.platform) {
    case 'darwin':
      return path.join(homeDir, 'Library', 'Application Support', identifier, 'openclaw', 'workspace');
    case 'win32':
      return path.join(process.env.APPDATA || path.join(homeDir, 'AppData', 'Roaming'), identifier, 'openclaw', 'workspace');
    default:
      return path.join(process.env.XDG_DATA_HOME || path.join(homeDir, '.local', 'share'), identifier, 'openclaw', 'workspace');
  }
}

async function resolveRuntimeWorkspaceDir() {
  const brandConfigPath = path.join(ROOT_DIR, 'apps/desktop/src-tauri/brand.generated.json');
  const brandConfig = await readJson(brandConfigPath);
  const appSlug = brandConfig.brandId || brandConfig.storageNamespace || 'caiclaw';
  return path.join(os.homedir(), '.openclaw', 'apps', appSlug, 'workspace');
}

function buildPdfBuffer(lines) {
  const stream = [
    'BT',
    '/F1 24 Tf',
    '72 740 Td',
    ...lines.flatMap((line, index) => {
      const escaped = line.replace(/\\/g, '\\\\').replace(/\(/g, '\\(').replace(/\)/g, '\\)');
      if (index === 0) {
        return [`(${escaped}) Tj`];
      }
      return ['0 -28 Td', `(${escaped}) Tj`];
    }),
    'ET',
  ].join('\n');

  const objects = [
    '<< /Type /Catalog /Pages 2 0 R >>',
    '<< /Type /Pages /Kids [3 0 R] /Count 1 >>',
    '<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources << /Font << /F1 5 0 R >> >> /Contents 4 0 R >>',
    `<< /Length ${Buffer.byteLength(stream, 'utf8')} >>\nstream\n${stream}\nendstream`,
    '<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>',
  ];

  let pdf = '%PDF-1.4\n';
  const offsets = [0];

  for (let index = 0; index < objects.length; index += 1) {
    offsets.push(Buffer.byteLength(pdf, 'utf8'));
    pdf += `${index + 1} 0 obj\n${objects[index]}\nendobj\n`;
  }

  const xrefOffset = Buffer.byteLength(pdf, 'utf8');
  pdf += `xref\n0 ${objects.length + 1}\n`;
  pdf += '0000000000 65535 f \n';
  for (let index = 1; index < offsets.length; index += 1) {
    pdf += `${String(offsets[index]).padStart(10, '0')} 00000 n \n`;
  }
  pdf += `trailer\n<< /Size ${objects.length + 1} /Root 1 0 R >>\nstartxref\n${xrefOffset}\n%%EOF\n`;
  return Buffer.from(pdf, 'utf8');
}

function buildMarkdownFixture() {
  return `# Artifact Preview Self-Test

This markdown file is generated by \`scripts/artifact-preview-self-test.mjs\`.

## Goal

- Verify the left chat artifact card becomes clickable
- Verify the right pane opens in markdown mode
- Verify the header action can still open the original file

## Checklist

1. Ask the agent to read this file
2. Click the generated artifact card in the chat thread
3. Confirm the right pane renders markdown content
`;
}

