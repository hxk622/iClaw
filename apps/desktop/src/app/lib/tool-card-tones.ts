export function isToolCardTone(value: string | null | undefined): value is ToolCardTone {
  return TOOL_CARD_TONE_PRESETS.some((preset) => preset.value === value);
}

export function resolveToolCardTonePreset(value: ToolCardTone): ToolCardTonePreset {
  return TOOL_CARD_TONE_PRESETS.find((preset) => preset.value === value) ?? TOOL_CARD_TONE_PRESETS[0];
}
