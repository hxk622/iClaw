function buildAgentReachMetadata(input: {
  slug: string;
  version?: string;
  channel: string;
  sourceLabel?: string;
  requiresApiKey?: boolean;
  setupSchema?: Record<string, unknown> | null;
}): Record<string, unknown> {
  const metadata: Record<string, unknown> = {
    source_label: input.sourceLabel || 'Agent Reach',
    provider: 'agent-reach',
    channel: input.channel,
    source_url: AGENT_REACH_SOURCE_URL,
    source_kind: 'managed-cloud-artifact',
    requires_api_key: input.requiresApiKey === true,
    [CLOUD_SKILL_ARTIFACT_OBJECT_KEY_FIELD]: buildCloudSkillArtifactObjectKey({
      slug: input.slug,
      version: input.version || '1.0.0',
      artifactFormat: 'tar.gz',
    }),
  };
  if (input.setupSchema) {
    metadata.setup_schema = input.setupSchema;
  }
  return metadata;
}

function buildSecretSetupSchema(fields: Array<{
  key: string;
  label: string;
  placeholder?: string;
  helpText?: string;
}>): Record<string, unknown> {
  return {
    version: 1,
    fields: fields.map((field) => ({
      key: field.key,
      label: field.label,
      type: 'secret',
      required: true,
      inject_as: field.key,
      placeholder: field.placeholder || '',
      help_text: field.helpText || '',
    })),
  };
}

